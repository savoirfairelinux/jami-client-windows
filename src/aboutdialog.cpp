#include "aboutdialog.h"
#include "ui_aboutdialog.h"

#include "utils.h"
#include "version.h"
#include "mainwindow.h"

AboutDialog::AboutDialog(QWidget *parent) :
    PopupWidget(
        parent,
        Qt::white,
        PopupDialog::SpikeLabelAlignment::None),
    ui(new Ui::AboutDialog)
{
    ui->setupUi(this);

    this->setFixedSize(this->width(),this->height());
    ui->gitVersionLabel->setText(QString("%1: %2")
        .arg(isBeta ? tr("(BETA) version") : tr("version"), QString(VERSION_STRING)));

    ui->logTextEdit->insertHtml(Utils::getChangeLog());

}

AboutDialog::~AboutDialog()
{
    delete ui;
}

void
AboutDialog::showEvent(QShowEvent* event)
{
    QWidget::showEvent(event);
    auto cp = getContainer()->parentWidget();
    if (auto mainWindow = qobject_cast<MainWindow*>(cp)) {
        mainWindow->darken();
    }
}

void
AboutDialog::hideEvent(QHideEvent* event)
{
    QWidget::hideEvent(event);
    auto cp = getContainer()->parentWidget();
    if (auto mainWindow = qobject_cast<MainWindow*>(cp)) {
        mainWindow->lighten();
    }
}

void
AboutDialog::on_changelogButton_clicked()
{
    ui->logTextEdit->clear();
    ui->logTextEdit->insertHtml(Utils::getChangeLog());
}

void
AboutDialog::on_closeAboutDialogButton_clicked()
{
    getContainer()->accept();
}

